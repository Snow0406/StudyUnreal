#pragma region GAME
void Initialize() {
	player = (Player*)malloc(sizeof(Player));
	player->x = 10;
	player->y = 38;
	player->h = player->y;
	player->color = YELLOW;
	player->shape = "¿Ê";
}



void Progress() {
	if (GetAsyncKeyState(VK_LEFT))
	{
		player->x--;
	}

	if (GetAsyncKeyState(VK_RIGHT))
	{
		player->x++;
	}

	if (GetAsyncKeyState(VK_SPACE))
	{
		jump = true;
		time = 0.0f;
		player->h = player->y;
	}

	if (jump)
	{
		int pos1 = player->h - V0 * time + 0.5f * G * time * time; // ÀÌÀü
		time += 0.2f;	// time Áõ°¡ 
		int pos2 = player->h - V0 * time + 0.5f * G * time * time; // ´ëÀÔ

		int distance = pos2 - pos1;

		bool collision = false;

		if (distance > 0)
		{
			for (int i = 0; i < distance; i++)
			{
				if (map[player->y + i][player->x] == 1)
				{
					collision = true;
					player->h = player->y + i - 1;

					break;
				}
			}
		}

		if (collision)
		{
			jump = false;
			player->y = player->h;
		}
		else
		{
			player->y += distance;
		}

		if (player->y > player->h)
		{
			jump = false;
			player->y = player->h;
		}
	}


}
void Render() {
	for (int y = 0; y < 40; y++)
	{
		for (int x = 0; x < 40; x++)
		{
			switch (map[y][x])
			{
			case 1:
				SetPosition(x, y);
				SetColor(WHITE);
				printf("¡á");
				break;
			default:
				break;
			}
		}
	}

	SetPosition(player->x, player->y);
	SetColor(player->color);
	printf(player->shape);
}
void Release() {
	if (player != nullptr)
	{
		free(player);
		player = nullptr;
	}
}
