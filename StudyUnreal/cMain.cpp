struct Obj
{
	int x;
	int y;
	Color color;
	const char* shape[3];
};
Obj* player = nullptr;

void Initialize();
void Progress();
void Render();
void Release();
void Collision();

KeyState key = NONE;

int main() {
	HideCursor();
	ULONGLONG deltatime = GetTickCount64();
	Initialize();


	while (true)
	{
		if (deltatime + 50 <= GetTickCount64())
		{
			system("cls");
			Progress();
			Render();
			deltatime = GetTickCount64();
		}
	}

	Release();
	return 0;
}

void Initialize() {
	player = (Obj*)malloc(sizeof(Obj));
	player->x = 10;
	player->y = 10;
	player->color = BLUE;
	player->shape[0] = "ЁсЁсЁсЁс";
	player->shape[1] = "ЁсЁсЁсЁс";
	player->shape[2] = "ЁсЁсЁсЁс";

}

void Progress() {
	if (GetAsyncKeyState(VK_LEFT))
	{
		player->x--;
		key = LEFT;
	}
	if (GetAsyncKeyState(VK_RIGHT))
	{
		player->x++;
		key = RIGHT;
	}
	if (GetAsyncKeyState(VK_UP))
	{
		player->y--;
		key = UP;
	}
	if (GetAsyncKeyState(VK_DOWN))
	{
		player->y++;
		key = DOWN;
	}
	Collision();
}

void Render() {
	for (int y = 0; y < 40; y++)
	{
		for (int x = 0; x < 40; x++)
		{
			switch (map[y][x])
			{
			case 1:
				Write(x, y, "Ёс", WHITE);
				break;
			case 2:
				break;
			default:
				break;
			}
		}
	}

	for (int i = 0; i < 3; i++)
	{
		Write(player->x, player->y + i, player->shape[i], player->color);
	}

}

void Release() {
}

void Collision() {
	// УцЕЙ УГИЎ
	int X[] = { player->x, player->x + 1, player->x + 2 , player->x + 3 };
	int Y[] = { player->y, player->y + 1, player->y + 2 , player->y + 3 };

	for (int i = 0; i < 4; i++) //X
	{
		for (int j = 0; j < 4; j++) //Y
		{
			if (map[X[i]][Y[j]] == 1) //ИИОр БИСЖЙААњ ИИГЕДйИщ
			{
				switch (key)
				{
					//ХАИІ ОЦЙЋРдЗТ ОШЧпРЛЖЇ ИЎХЯ
				case NONE:
					break;
				case LEFT: player->x++;
					break;
				case RIGHT: player->x--;
					break;
				case UP: player->y++;
					break;
				case DOWN: player->y--;
					break;

				default:
					break;
				}
			}

		}
	}
}

#pragma region WinAPI
//БєЙкБєЙк АХИЎДТ ФПМ­ ОјОжСжДТ ЧдМі
void HideCursor() {
	CONSOLE_CURSOR_INFO info;
	info.dwSize = 1;
	info.bVisible = false;

	SetConsoleCursorInfo(GetStdHandle(STD_OUTPUT_HANDLE), &info);
}

//X,YАЊ ЙоОЦМ­ ФПМ­РЧ СТЧЅИІ РЬЕПНУФбСжДТ ЧдМі
void SetPosition(int x, int y) {
	COORD pos;
	pos.X = x * 2;
	pos.Y = y;

	SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), pos);
}

//colorАЊ ЙоОЦМ­ ЛіБђ ЙйВуСжДТ ОЦРЬ
void SetColor(int color) {
	SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), color);
}
void Write(int x, int y, const char* shape, int color) {
	SetColor(color);
	SetPosition(x, y);
	printf(shape);

}
